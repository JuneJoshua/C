/*
Uses conversion characters and prints them
on the screen
*/

#include <stdlib.h>
#include <stdio.h>
int main()
{
    //String
    printf("%s is the best teammate ever!\n", "Samus Aran");
    
    //Integer
    printf("I have played 'Nobody' at least %i times\n", 400);
    
    //Float
    printf("Pi equals %f\n", 3.14159);
    return 0;
}
